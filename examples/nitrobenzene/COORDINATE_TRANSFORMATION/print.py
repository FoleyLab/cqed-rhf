import io

def format_coords_to_list(raw_data_string, var_name="para_coords"):
    """
    Parses raw coordinate data and formats it into a clean Python list string.
    - Keeps only Symbol, X, Y, Z
    - Discards Mass
    - Normalizes Symbols (e.g., BR -> Br)
    - Formats coords to 8 decimal places
    """
    formatted_lines = []
    
    # Process line by line
    for line in raw_data_string.strip().split('\n'):
        parts = line.split()
        if len(parts) >= 4:
            # 1. Normalize Symbol (e.g., 'BR' -> 'Br', 'C' -> 'C')
            symbol = parts[0].capitalize()
            
            # 2. Convert and format coordinates
            try:
                x = f"{float(parts[1]):15.8f}"
                y = f"{float(parts[2]):15.8f}"
                z = f"{float(parts[3]):15.8f}"
                
                # 3. Create the formatted string line
                formatted_lines.append(f'    "{symbol:<2} {x} {y} {z}"')
            except ValueError:
                continue

    # 4. Construct the final Python list block
    output = f"{var_name} = [\n"
    output += ",\n".join(formatted_lines)
    output += "\n]"
    
    return output

